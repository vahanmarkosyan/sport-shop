const DICT: Record<Locale, Dict> = { hy, en, ru };

export function t(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  let s = DICT[locale][key] ?? DICT[DEFAULT_LOCALE][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

// Collection title localized by slug; falls back to the DB title
// (custom admin-created collections keep their own name).
export function collectionTitle(locale: Locale, slug: string, fallback: string): string {
  const key = `col.${slug}`;
  const val = DICT[locale][key];
  return val ?? fallback;
}

